public class Lagu {
    private String judul;
    private String durasi;

    public Lagu(String judul, String durasi) {
        this.judul = judul;
        this.durasi = durasi;
    }
    public String getJudul() {
        return judul;
    }
    public String getDurasi() {
        return durasi;
    }
}
